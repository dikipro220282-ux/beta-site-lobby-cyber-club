const sections = document.querySelectorAll(
".card,.rate-card,.stat-card,.media-photo,.media-video,.contacts-content"
);

sections.forEach(section => {
section.classList.add("reveal");
});

const observer = new IntersectionObserver(entries => {
entries.forEach(entry => {
if (entry.isIntersecting) {
entry.target.classList.add("active");
}
});
}, {
threshold: 0.15
});

sections.forEach(section => {
observer.observe(section);
});

const glow = document.createElement("div");
glow.className = "cursor-glow";
document.body.appendChild(glow);

document.addEventListener("mousemove", event => {
glow.style.left = `${event.clientX}px`;
glow.style.top = `${event.clientY}px`;
});

const hero = document.querySelector(".hero-content");

if (hero) {
document.addEventListener("mousemove", event => {
const x = (window.innerWidth / 2 - event.clientX) / 50;
const y = (window.innerHeight / 2 - event.clientY) / 50;
hero.style.transform = `translate(${x}px,${y}px)`;
});
}

const navLinks = document.querySelectorAll('nav a[href^="#"]');

navLinks.forEach(link => {
link.addEventListener("click", event => {
event.preventDefault();
const target = document.querySelector(link.getAttribute("href"));

if (target) {
window.scrollTo({
top: target.offsetTop - 110,
behavior: "smooth"
});
}
});
});

window.addEventListener("scroll", () => {
let current = "";

document.querySelectorAll("section[id]").forEach(section => {
const sectionTop = section.offsetTop - 150;
if (window.scrollY >= sectionTop) {
current = section.id;
}
});

navLinks.forEach(link => {
link.style.color = "#fff";
if (link.getAttribute("href") === `#${current}`) {
link.style.color = "#a855f6";
}
});
});

const langBtn = document.getElementById("langBtn");
const storedLang = getSavedLanguage();
let currentLang = storedLang === "en" ? "en" : "ru";

function getSavedLanguage() {
try {
return window.localStorage.getItem("siteLang");
} catch (error) {
return null;
}
}

function saveLanguage(lang) {
try {
window.localStorage.setItem("siteLang", lang);
} catch (error) {
// Some privacy modes block storage; switching still works for the current page.
}
}

function setLanguage(lang) {
currentLang = lang;
document.documentElement.lang = lang;

document.querySelectorAll("[data-i18n]").forEach(element => {
const key = element.dataset.i18n;
const value = translations[lang][key];
if (value) {
element.textContent = value;
}
});

document.querySelectorAll("[data-i18n-alt]").forEach(element => {
const key = element.dataset.i18nAlt;
const value = translations[lang][key];
if (value) {
element.setAttribute("alt", value);
}
});

if (langBtn) {
const nextLang = lang === "ru" ? "en" : "ru";
langBtn.textContent = nextLang.toUpperCase();
langBtn.setAttribute("aria-label", lang === "ru" ? "Switch to English" : "Переключить на русский");
}

saveLanguage(lang);
}

if (langBtn) {
langBtn.addEventListener("click", () => {
setLanguage(currentLang === "ru" ? "en" : "ru");
});
}

setLanguage(currentLang);
